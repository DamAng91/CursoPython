#Tambien al debanado de cadenas se lo conoce como sub-strings 

cadena = "Este es un ejemplo de substring (debanado de cadenas)"

#print(len(cadena)) #len devuelve el tamaño de la cadena

print(cadena[2]) #Devuelve el caracter en la posición 2 (t) comenzando desde 0. 
print(cadena[0:9]) #Devuelve los caracteres desde la posición 0 hasta la posición 9.
print(cadena[:20]) #Devuelve los caracteres desde la posición 0 hasta la posición 20.